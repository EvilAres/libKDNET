BYTE* cbc_decrypt(BYTE* ciphered, int ciphered_len, WORD* w, BYTE* iv){
	BYTE current_unciphered_block[16];
	BYTE last_ciphered_block[16];
	BYTE current_ciphered_block[16];
	BYTE *unciphered;
	int j;
	int i;
	unciphered = (BYTE*)malloc(sizeof(BYTE)*ciphered_len);
	memcpy(last_ciphered_block, iv, 16);

	/*printf("IV : ");
	for(i=0; i<16; i++){
		printf("%02x ", iv[i]);
	}
	printf("\n");
	printf("First block : ");
	for(i=0; i<16; i++){
		printf("%02x ", ciphered[i]);
	}
	printf("\n");*/

	
	for(i=0; i<ciphered_len/16; i++){
		memcpy(current_ciphered_block, ciphered+(i*16), 16);
		aes_decrypt(current_ciphered_block, current_unciphered_block, w, 256);
		for(j=0; j<16; j++){
			current_unciphered_block[j] = current_unciphered_block[j]^last_ciphered_block[j];
		}
		memcpy(last_ciphered_block, current_ciphered_block, 16);
		
		for(j=0; j<16; j++){
			printf("%02x ", current_unciphered_block[j]);
		}
		printf("\n");
		memcpy(unciphered+(i*16), current_unciphered_block, 16);
	}
	return unciphered;
}



